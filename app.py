import streamlit as st
import random

st.set_page_config(page_title="今日の名言", page_icon="✨")

st.title("✨ 今日を彩る名言アプリ")
st.write("ボタンを押すと、あなたにぴったりの名言が表示されます。")

# 名言のリスト
quotes = [
    "「失敗は成功のもとである。」 - エジソン",
    "「あきらめたらそこで試合終了だよ。」 - 安西先生",
    "「雲の向こうは、いつも青空。」 - ルイーザ・メイ・オルコット",
    "「一歩踏み出せるなら、もう一歩も踏み出せる。」 - トッド・スキナー",
    "「あなたの心が正しいと思うことをしなさい。」 - エレノア・ルーズベルト",
    "「昨日の自分より、今日の自分が素晴らしい。」",
    "「楽しんでやるのが一番の近道。」",
    "「やってみせ、言って聞かせて、させてみせ、ほめてやらねば、人は動かじ。」 - 山本五十六"
]

# ボタンが押されたらランダムに選ぶ
if st.button("新しい名言を受け取る"):
    selected_quote = random.choice(quotes)
    st.snow() # お祝いの風船
    st.warning(selected_quote)
else:
    st.info("下のボタンをクリックしてください！")